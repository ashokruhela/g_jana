package in.gurujana.standalone.model;

public class Email {
	
	private String emailId;
	private String fromEmail;
	private String toEmail;
	private String ccEmail;
	private String bccEmail;
	private String subject;
	private String contentFilePath;
	private boolean isEmailAttachment;
	private Long emailAttachments;
	private String emailCreatedDate;
	private String sendDate;
	private String contentType;
	private String emailStatus;
	private String clientId;
	private String mailBody;
	private String attchedFiles;
	
	
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getFromEmail() {
		return fromEmail;
	}
	public void setFromEmail(String fromEmail) {
		this.fromEmail = fromEmail;
	}
	public String getToEmail() {
		return toEmail;
	}
	public void setToEmail(String toEmail) {
		this.toEmail = toEmail;
	}
	public String getCcEmail() {
		return ccEmail;
	}
	public void setCcEmail(String ccEmail) {
		this.ccEmail = ccEmail;
	}
	public String getBccEmail() {
		return bccEmail;
	}
	public void setBccEmail(String bccEmail) {
		this.bccEmail = bccEmail;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContentFilePath() {
		return contentFilePath;
	}
	public void setContentFilePath(String contentFilePath) {
		this.contentFilePath = contentFilePath;
	}
	public boolean isEmailAttachment() {
		return isEmailAttachment;
	}
	public void setEmailAttachment(boolean isEmailAttachment) {
		this.isEmailAttachment = isEmailAttachment;
	}
	public Long getEmailAttachments() {
		return emailAttachments;
	}
	public void setEmailAttachments(Long emailAttachments) {
		this.emailAttachments = emailAttachments;
	}
	public String getEmailCreatedDate() {
		return emailCreatedDate;
	}
	public void setEmailCreatedDate(String emailCreatedDate) {
		this.emailCreatedDate = emailCreatedDate;
	}
	public String getSendDate() {
		return sendDate;
	}
	public void setSendDate(String sendDate) {
		this.sendDate = sendDate;
	}
	public String getContentType() {
		return contentType;
	}
	public void setContentType(String contentType) {
		this.contentType = contentType;
	}
	public String getEmailStatus() {
		return emailStatus;
	}
	public void setEmailStatus(String emailStatus) {
		this.emailStatus = emailStatus;
	}
	public String getClientId() {
		return clientId;
	}
	public void setClientId(String clientId) {
		this.clientId = clientId;
	}
	public String getMailBody() {
		return mailBody;
	}
	public void setMailBody(String mailBody) {
		this.mailBody = mailBody;
	}
	public String getAttchedFiles() {
		return attchedFiles;
	}
	public void setAttchedFiles(String attchedFiles) {
		this.attchedFiles = attchedFiles;
	}
	 

	 
	
}
